import { BrowserRouter as Router, Routes, Route } from 'react-router-dom';
import { AuthProvider } from './context/AuthContext';
import { ProtectedRoute } from './components/ProtectedRoute';

import Navbar from './components/Navbar';
import Home from './pages/Home';
import Login from './pages/Login';
import Register from './pages/Register';
import Reserve from './pages/Reserve';
import MyReservations from './pages/MyReservations';
import Waitlist from './pages/Waitlist';
import StaffDashboard from './pages/staff/StaffDashboard';

function App() {
  return (
    <Router>
      <AuthProvider>
        <div style={{ minHeight: '100vh', display: 'flex', flexDirection: 'column' }}>
          <Navbar />
          <main style={{ flexGrow: 1, width: '100%', maxWidth: '1280px', margin: '0 auto', padding: '32px 16px', boxSizing: 'border-box' }}>
            <Routes>
              {/* Public Views */}
              <Route path="/" element={<Home />} />
              <Route path="/login" element={<Login />} />
              <Route path="/register" element={<Register />} />

              {/* Secure Booking Paths */}
              <Route path="/reserve" element={<ProtectedRoute><Reserve /></ProtectedRoute>} />
              <Route path="/my-reservations" element={<ProtectedRoute><MyReservations /></ProtectedRoute>} />
              <Route path="/waitlist" element={<ProtectedRoute><Waitlist /></ProtectedRoute>} />

              {/* Administrative Floor Control Dashboard */}
              <Route path="/staff/dashboard" element={
                <ProtectedRoute allowedRoles={['staff', 'admin']}>
                  <StaffDashboard />
                </ProtectedRoute>
              } />
            </Routes>
          </main>
        </div>
      </AuthProvider>
    </Router>
  );
}

export default App;